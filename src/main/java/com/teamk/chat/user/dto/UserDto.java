package com.teamk.chat.user.dto;



public class UserDto {

	private int member_no;
	private String member_id;
	private String member_pw;
	private String member_name;
	private String member_addr;
	private String member_mobile;
	private String member_channel;
	private String member_auth;
	private String member_exit;
	
	public int getMember_no() {
		return member_no;
	}
	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getMember_pw() {
		return member_pw;
	}
	public void setMember_pw(String member_pw) {
		this.member_pw = member_pw;
	}
	public String getMember_name() {
		return member_name;
	}
	public void setMember_name(String member_name) {
		this.member_name = member_name;
	}
	public String getMember_addr() {
		return member_addr;
	}
	public void setMember_addr(String member_addr) {
		this.member_addr = member_addr;
	}
	public String getMember_mobile() {
		return member_mobile;
	}
	public void setMember_mobile(String member_mobile) {
		this.member_mobile = member_mobile;
	}
	public String getMember_channel() {
		return member_channel;
	}
	public void setMember_channel(String member_channel) {
		this.member_channel = member_channel;
	}
	public String getMember_auth() {
		return member_auth;
	}
	public void setMember_auth(String member_auth) {
		this.member_auth = member_auth;
	}
	public String getMember_exit() {
		return member_exit;
	}
	public void setMember_exit(String member_exit) {
		this.member_exit = member_exit;
	}
	
}
